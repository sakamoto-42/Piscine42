#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;

	if (argc - 1 >= 1)
	{
		i = argc - 1;
		while (i >= 1)
		{
			while (*argv[i] != '\0')
			{
				write(1, argv[i], 1);
				argv[i]++;
			}
			write(1, "\n", 1);
			i--;
		}
	}
	return (0);
}
